package glaf.batch.exception.sys;


public class BackupFileFailedException extends SysException {

	public BackupFileFailedException() {
		super();
	}

	public BackupFileFailedException(String message, Throwable cause) {
		super(message, cause);
	}

	public BackupFileFailedException(String message) {
		super(message);
	}

	public BackupFileFailedException(Throwable cause) {
		super(cause);
	}

}
