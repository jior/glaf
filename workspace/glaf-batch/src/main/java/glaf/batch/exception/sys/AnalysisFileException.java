package glaf.batch.exception.sys;


public class AnalysisFileException extends SysException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public AnalysisFileException() {
		super();
	}

	public AnalysisFileException(String message, Throwable cause) {
		super(message, cause);
	}

	public AnalysisFileException(String message) {
		super(message);
	}

	public AnalysisFileException(Throwable cause) {
		super(cause);
	}

}
