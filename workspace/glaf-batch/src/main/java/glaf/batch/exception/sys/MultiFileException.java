package glaf.batch.exception.sys;


public class MultiFileException  extends SysException{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public MultiFileException() {
		super();
	}

	public MultiFileException(String message, Throwable cause) {
		super(message, cause);
	}

	public MultiFileException(String message) {
		super(message);
	}

	public MultiFileException(Throwable cause) {
		super(cause);
	}
}
