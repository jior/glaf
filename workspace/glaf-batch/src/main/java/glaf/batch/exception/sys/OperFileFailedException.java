package glaf.batch.exception.sys;


public class OperFileFailedException extends SysException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public OperFileFailedException() {
		super();
	}

	public OperFileFailedException(String message, Throwable cause) {
		super(message, cause);
	}

	public OperFileFailedException(String message) {
		super(message);
	}

	public OperFileFailedException(Throwable cause) {
		super(cause);
	}

}
